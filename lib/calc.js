// lib/calc.js - 이중 볼린저밴드 + 매집 점수 계산

// ── 이중 볼린저밴드 계산 ───────────────────────────────────────
// BB1 = 이동평균 ± 2표준편차 (외부밴드)
// BB2 = 이동평균 ± 1표준편차 (내부밴드)
function 볼린저밴드계산(현재가, 고가, 저가, 전일가, 시가) {
  const 데이터 = [현재가, 고가, 저가, 전일가, 시가].filter(v => v > 0);
  const n = 데이터.length || 1;
  const 이평 = 데이터.reduce((a, b) => a + b, 0) / n;
  const 표준편차 = Math.max(
    Math.sqrt(데이터.reduce((s, v) => s + (v - 이평) ** 2, 0) / n),
    현재가 * 0.01
  );
  return {
    이평선:  +이평.toFixed(3),
    bb1상단: +(이평 + 2 * 표준편차).toFixed(3),
    bb1하단: +(이평 - 2 * 표준편차).toFixed(3),
    bb2상단: +(이평 + 1 * 표준편차).toFixed(3),
    bb2하단: +(이평 - 1 * 표준편차).toFixed(3),
    밴드폭:  +((4 * 표준편차 / 이평) * 100).toFixed(1),
  };
}

// ── BB 구간 판정 ──────────────────────────────────────────────
function bb구간판정(현재가, bb) {
  if (!bb) return null;
  if (현재가 >= bb.bb1상단) return { id:1, 명칭:"BB1 상단 돌파",    색:"#cc0000", 신호:"과매수 — 추격 주의" };
  if (현재가 >= bb.bb2상단) return { id:2, 명칭:"★ BB 매수 구간",  색:"#007700", 신호:"강세 — 진입 적합" };
  if (현재가 >= bb.이평선)  return { id:3, 명칭:"이평선 위 (중립)", 색:"#886600", 신호:"관망" };
  if (현재가 >= bb.bb2하단) return { id:4, 명칭:"이평선 아래(중립)",색:"#995500", 신호:"조정 중" };
  if (현재가 >= bb.bb1하단) return { id:5, 명칭:"BB 매도 구간",    색:"#cc3300", 신호:"반등 대기" };
  return                          { id:6, 명칭:"BB1 하단 돌파",   색:"#660099", 신호:"극도 과매도" };
}

// ── 매집 점수 ────────────────────────────────────────────────
function 매집점수계산(현재가, 변동률, vwap, bb, 구간) {
  let 점수 = 0;
  const 신호목록 = [];

  if (구간?.id === 2)            { 점수 += 30; 신호목록.push("✅ BB 매수구간 (BB2~BB1 상단 사이)"); }
  if (구간?.id === 1)            { 점수 += 12; 신호목록.push("⚡ BB1 상단 돌파 (강세)"); }
  if (bb?.밴드폭 <= 6)           { 점수 += 20; 신호목록.push("🎯 밴드 수축 — 매집 직전 신호!"); }
  else if (bb?.밴드폭 <= 10)     { 점수 +=  8; 신호목록.push("📊 밴드 좁아짐"); }
  if (Math.abs(변동률) <= 3)     { 점수 += 15; 신호목록.push("🔵 횡보 (매집 패턴)"); }
  if (현재가 > vwap)             { 점수 += 15; 신호목록.push("✅ VWAP 위 — 매수 우위"); }
  if (변동률 >= 10)              { 점수 += 10; 신호목록.push(`🔥 +${변동률}% 급등`); }

  const 최종점수 = Math.min(점수, 100);
  return {
    점수: 최종점수,
    신호목록,
    등급: 최종점수 >= 60 ? { 명칭:"🔴 강력 매집", 색:"#cc0000" }
        : 최종점수 >= 40 ? { 명칭:"🟡 매집 가능", 색:"#886600" }
        : 최종점수 >= 20 ? { 명칭:"🔵 관심",       색:"#005599" }
        :                  { 명칭:"⬜ 미약",        색:"#999999" },
  };
}

// ── 메인: 종목 데이터 생성 ────────────────────────────────────
// ★ 중요: q = API에서 받은 json.data 객체
// q.c = 현재가, q.pc = 전일가, q.h = 고가, q.l = 저가, q.o = 시가
export function buildStock(ticker, q) {
  // 기본값 처리
  const 현재가 = +q.c;
  const 전일가  = q.pc  || 현재가;
  const 고가    = q.h   || 현재가 * 1.02;
  const 저가    = q.l   || 현재가 * 0.98;
  const 시가    = q.o   || 전일가;

  if (현재가 <= 0) return null;

  const 변동률 = 전일가 > 0
    ? +((현재가 - 전일가) / 전일가 * 100).toFixed(2)
    : 0;

  const vwap = +((고가 + 저가 + 현재가) / 3).toFixed(2);
  const atr  = +((고가 - 저가) * 1.2).toFixed(3);
  const atr비율 = 현재가 > 0 ? +(atr / 현재가 * 100).toFixed(1) : 0;

  const bb   = 볼린저밴드계산(현재가, 고가, 저가, 전일가, 시가);
  const 구간 = bb구간판정(현재가, bb);
  const 매집 = 매집점수계산(현재가, 변동률, vwap, bb, 구간);

  // 매매 레벨
  const 진입가 = +(vwap * 1.005).toFixed(2);
  const 리스크  = +(Math.max(atr * 1.5, 현재가 * 0.025)).toFixed(2);
  const 손절가  = +(진입가 - 리스크).toFixed(2);
  const 목표1  = +(진입가 + 리스크 * 2.5).toFixed(2);
  const 목표2  = +(진입가 + 리스크 * 4.0).toFixed(2);

  return {
    ticker,
    현재가: +현재가.toFixed(2),
    전일가: +전일가.toFixed(2),
    고가:   +고가.toFixed(2),
    저가:   +저가.toFixed(2),
    시가:   +시가.toFixed(2),
    변동률,
    vwap,
    atr,
    atr비율,
    bb,
    bb구간: 구간,
    매집점수: 매집.점수,
    매집신호: 매집.신호목록,
    매집등급: 매집.등급,
    진입가,
    손절가,
    목표1,
    목표2,
    리스크,
  };
}
